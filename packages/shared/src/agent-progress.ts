/**
 * A single step in an agent's progress.
 *
 * The `type` field is the union of desktop and mobile step types.
 * The `status` field is the union of desktop and mobile status values.
 */
export interface AgentProgressStep {
  id: string
  type:
    | 'thinking'
    | 'tool_call'
    | 'tool_result'
    | 'completion'
    | 'tool_approval'
    // Mobile-specific types:
    | 'response'
    | 'error'
    | 'pending_approval'
  title: string
  description?: string
  status:
    | 'pending'
    | 'in_progress'
    | 'completed'
    | 'error'
    // Desktop-specific statuses:
    | 'awaiting_approval'
  timestamp: number

  // --- Shared fields ---
  llmContent?: string
  toolCall?: ToolCall
  toolResult?: ToolResult

  // --- Mobile-specific ---
  /** Raw content string (mobile uses this instead of llmContent in some cases) */
  content?: string

  // --- Desktop-specific ---
  approvalRequest?: {
    approvalId: string
    toolName: string
    arguments: any
  }
  /** If this step is a delegation to a sub-agent */
  delegation?: ACPDelegationProgress
  executionStats?: {
    durationMs?: number
    totalTokens?: number
    toolUseCount?: number
    inputTokens?: number
    outputTokens?: number
    cacheHitTokens?: number
  }
  subagentId?: string
}

