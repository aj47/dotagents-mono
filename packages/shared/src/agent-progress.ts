/**
 * Agent step summary generated by a weak/summarization model.
 */
export interface AgentStepSummary {
  id: string
  sessionId: string
  stepNumber: number
  timestamp: number

  /** What the agent just did (single line) */
  actionSummary: string

  /**
   * Durable note candidates extracted from this step.
   * These should be reusable in future sessions (preferences, constraints,
   * decisions, facts, insights), NOT step-by-step telemetry.
   */
  noteCandidates?: string[]

  // --- Desktop-specific metadata ---
  importance?: 'low' | 'medium' | 'high' | 'critical'
  savedToKnowledgeNote?: boolean
  userNotes?: string
  tags?: string[]

  // Legacy fields (kept for backward compatibility)
  keyFindings?: string[]
  nextSteps?: string
  decisionsMade?: string[]
}

